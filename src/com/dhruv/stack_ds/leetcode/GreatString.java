package com.dhruv.stack_ds.leetcode;

public class GreatString {
    public static void main(String[] args) {
        String str = "aA";
        int x = str.charAt(0);
        int y = str.charAt(1);
        System.out.println(x);
        System.out.println(y);
        System.out.println(Math.abs(x-y));
    }
}
